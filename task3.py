# Узнайте у пользователя число n. Найдите
# сумму чисел n + nn + nnn. Например,
# пользователь ввёл число 3. Считаем 3 + 33 + 333 = 369.
a = int(input("Введите целое число: "))
n1 = int("%s" % a)
n2 = int("%s%s" % (a, a))
n3 = int("%s%s%s" % (a, a, a))
print("Ответ:")
print(n1 + n2 + n3)
